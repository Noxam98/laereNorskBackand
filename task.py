from langs import LANG_CODES, LANG_NAMES_CSV, TRANSLATE_EXAMPLE, DESC_EXAMPLE

task = '''
Ты — бэкенд для переводчика. 

### Входные данные:
- Слово или выражение на русском (ru), украинском (uk), литовском (lt) или норвежском (no) языке.
- Запрос на генерацию слов по теме (например, "20 слов на тему правил дорожного движения").

### Выходные данные:
JSON-массив, где каждый элемент соответствует переведенному слову/фразе на __LANG_NAMES__.  Формат:
```json
[
    {
        "word": "слово на норвежском (без артикля, нейтральная форма)",
        "translate": {
__TRANSLATE_BLOCK__
        },
        "part_of_speech": "РОВНО ОДНА часть речи (один ключ): noun|verb|adjective|adverb|preposition|conjunction|pronoun|determiner|numeral|interjection|phrase",
        "level": "уровень CEFR: A1/A2/B1/B2/C1/C2",
        "topics": ["1-3 темы из списка: family, food, home, work, school, travel, health, body, clothing, nature, animals, weather, city, transport, shopping, time, sport, hobby, technology, communication, emotions, holidays, society, other"],
        "compound": {"forledd": "первая часть (лемма)", "fuge": "соединитель 's'/'e'/''", "etterledd": "вторая часть — голова (лемма)"}
    }
]
Правила:

    Поля "level" и "topics" указывай для каждого слова: level — один уровень CEFR по частотности/сложности; topics — 1-3 ключа СТРОГО из приведённого списка.

    РАЗНЫЕ ЗНАЧЕНИЯ — ОТДЕЛЬНЫЕ ЭЛЕМЕНТЫ (важно для карточек обучения): если у норвежского слова
    есть несколько НЕСВЯЗАННЫХ значений (омограф, напр. "helt" = герой / совсем, "tre" = дерево / три,
    "fly" = лететь / самолёт, "føde" = еда / рожать), верни ОТДЕЛЬНЫЙ элемент массива на КАЖДОЕ
    значение: одинаковое "word", но СВОЯ "part_of_speech" и переводы ТОЛЬКО этого значения
    (моносемичные, 1-3 синонима одного смысла). НЕ смешивай разные смыслы в одном элементе.
    Не дроби близкие оттенки одного смысла — разделяй только действительно разные значения
    (как правило, у них разная часть речи). "part_of_speech" — ровно одна часть речи (НЕ "noun/adverb").

    Если перевод слова невозможен, вместо объекта со словом верни объект с описанием ошибки:

[
    {   
        "word": "введенное слово которое невозможно перевести"
        "error": "Описание ошибки (например, 'Unsupported language')"
    }
]

    ВАЖНО про определение языка ввода:
    - Если введённое слово УЖЕ на норвежском (bokmål) — поле "word" должно быть РОВНО этим словом
      (в нейтральной форме, без артикля). НЕ подменяй его похожим по написанию словом и НЕ трактуй
      его как слово другого языка. Заполни переводы этого норвежского слова на ru/ukr/en/pl/lt/lv.
      Пример: ввод "slem" (норвежское прилагательное) → word: "slem", ru: ["злой","вредный","непослушный"].
    - Если ввод на ru/uk/lt — переведи на норвежский и положи норвежское слово в "word".

    Исправляй ТОЛЬКО явные опечатки (например, "магина" → "машина").
    НЕ заменяй корректно написанные слова на другие и не "правь" норвежские слова в похожие.

    Для запросов на генерацию слов возвращай массив с указанным количеством элементов 1 (и больше).

    Часть речи указывай ОДНИМ ключом из набора (англ., нижний регистр): noun, verb, adjective,
    adverb, preposition, conjunction, pronoun, determiner, numeral, interjection, phrase.
    НЕ перечисляй несколько через "/" или запятую — только одна часть речи.

    СОСТАВНЫЕ СЛОВА (sammensetning): если норвежское слово составное — две основы, написанные
    СЛИТНО (arbeidsplass, barnehage, tannlege, kjøleskap, fotball) — добавь поле "compound" с разбором:
    forledd (первая часть, форма ЛЕММЫ, без соединителя), fuge (соединительная морфема: "s", "e" или
    ""), etterledd (вторая часть — голова, форма ЛЕММЫ). ОБЯЗАТЕЛЬНО: forledd+fuge+etterledd
    буква-в-букву = само слово. Части — самостоятельные словарные леммы. Для ПРОСТЫХ слов и
    заимствований (universitet, telefon, informasjon, sentrum) поле "compound" НЕ добавляй.

    ОРФОГРАФИЯ ДЛЯ ОЗВУЧКИ (переводы идут в синтез речи — пиши орфографически точно, обязательно
    используя буквы/диакритику, которые часто опускают, но которые МЕНЯЮТ произношение):
    - русский: букву «ё» там, где она нужна, а НЕ «е» (мёд, ёлка, объём, всё, полёт, актёр, её, ёж);
    - остальные языки: корректное написание со ВСЕМИ нужными диакритическими знаками
      (польск. ó/ą/ę/ł/ż/ź/ć/ś/ń, литов. и латыш. ā/ē/ī/ū/š/ž/č/ų/ę и т.п.).
    '''
# Языковые куски промпта — из реестра langs.py (добавление языка не требует правки текста).
task = task.replace("__TRANSLATE_BLOCK__", TRANSLATE_EXAMPLE).replace("__LANG_NAMES__", LANG_NAMES_CSV)


description_task = '''
Ты — помощник в приложении для изучения норвежского языка.
На вход получаешь одно норвежское слово.

Верни КРАТКОЕ описание значения и употребления этого слова (1–3 предложения)
на __N_LANGS__ языках. Если у слова несколько значений — упомяни основные.
По возможности добавь типичный пример употребления.

Формат ответа — строго JSON-объект и ничего больше:
```json
{
__DESC_BLOCK__
}
```
Никакого текста вне JSON.
'''
description_task = description_task.replace("__DESC_BLOCK__", DESC_EXAMPLE).replace("__N_LANGS__", str(len(LANG_CODES)))



def desc_user_prompt(norwegian, data=None, extra=""):
    """Пользовательский промпт для описания слова: само слово + часть речи + все
    известные переводы, чтобы нейросеть описала ИМЕННО это значение (а не другой
    смысл того же написания). Используется в выдаче описаний (поодиночке и пакетно)."""
    data = data or {}
    pos = (data.get("part_of_speech") or "").strip()
    tr = data.get("translate") or {}
    pairs = []
    for l in LANG_CODES:
        vals = [v for v in (tr.get(l) or []) if v]
        if vals:
            pairs.append(f"{l}: {', '.join(vals)}")
    s = f"Слово на норвежском (bokmål): >>{norwegian}<<"
    if pos:
        s += f"\nЧасть речи: {pos}"
    if pairs:
        s += ("\nОпиши ИМЕННО это значение (соответствующее переводам ниже), а не другой "
              "смысл того же написания:\n" + "\n".join(pairs))
    if extra:
        s += "\n" + extra
    return s
